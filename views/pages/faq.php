<h2>Le site est en version alpha, que cela signifie-t-il ?</h2>
<p>
Un site en version alpha signifie que nous vérifions le bon fonctionnement de celui-ci. C'est une période pendant laquelle des bugs peuvent apparaitre. Même si nous mettons tout en mesure afin de vous offrir la meilleure qualité de service, nous ne pouvons pas garantir un fonctionnement optimum. A l'issue de la version alpha, ce sera la beta, puis l'ouverture au plus grand nombre ! Si vous souhaitez tester le service en avant première et contribuer à son amélioration, demandez-nous votre invitation.
</p>

<h2>Lorsque j'essai de créer plus de 15 alertes, j'ai un message d'erreur, pourquoi ?</h2>
<p>
Le site étant en version alpha (Cf. plus haut), l'objectif est de comprendre l'utilité, l'utilisation et le bon fonctionnement du service "La Bonne Alerte". Nous avons donc limité le nombre d'alertes afin de maitriser la charge du site. Si vous souhaitez débloquer cette limite, vous pouvez nous contacter.
</p>

<h2>Je ne souhaite plus recevoir de notification, comment faire ?</h2>
<p>
Nous sommes contents que vous ayez trouvé ce que vous cherchiez ! Chaque email de notification contient un lien de désactivation d'alerte. Dès que celui-ci est cliqué, vous cesserez de recevoir des emails de notification pour l'alerte correspondante. Les autres alertes restent actives.
</p>

<h2>Sur quels éléments la recherche se base-t-elle ?</h2>
<p>
	Actuellement, la recherche se base le titre de l'annonce. Si une annonce vous est envoyée, c'est qu'au  moins un des mots de votre recherche est contenu dans le titre de l'annonce. 
</p>

<h2>Je reçois trop de notifications pour une alerte donnée.</h2>
<p>
Effectivement, certaines alertes génèrent beaucoup de notifications. Nous souhaitons améliorer le système de correspondance "termes de recherche" &lt; = &gt; "annonce", nous serions donc ravis que vous nous indiquiez votre cas de figure.
</p>

